package PracticeClass;

public class numberTripleLoop {
    public static void main(String[] args) {
        for(int i=1; i<=9; i++){
            for(int j=0; j<=9; j++){
                for(int k=0; k<=9; k++){
                    System.out.print(i);
                    System.out.print(j);
                    System.out.print(k);
                    System.out.println("");
                }
            }
        }
    }
}
